func parseTable() {
	scanner := bufio.NewScanner(strings.NewReader(cp437_to_Unicode_table))
	for scanner.Scan() {
		text := scanner.Text()
		ps := strings.Split(text, "\t")

		fmt.Print(ps[0], ": '\\u")
		fmt.Print(strings.TrimPrefix(ps[1], "0x"), "', // ")
		fmt.Println(strings.TrimPrefix(ps[2], "#"))
	}
	if err := scanner.Err(); err != nil {
		log.Fatal(err)
	}
}

