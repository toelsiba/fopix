func parse() {

	data, err := ioutil.ReadFile("Codepage-437.png")
	if err != nil {
		log.Fatal(err)
	}
	m, _, err := image.Decode(bytes.NewReader(data))
	if err != nil {
		log.Fatal(err)
	}
	//bs := m.Bounds()
	//width := bs.Dx()
	//height := bs.Dy()

	x0, y0 := 8, 8
	dx, dy := 9, 16

	//	rss := make([][]rune, dy)
	//	for i := range rss {
	//		rss[i] = make([]rune, dx)
	//	}

	buf := new(bytes.Buffer)

	runeIndex := 0
	for y1 := 0; y1 < 8; y1++ {
		for x1 := 0; x1 < 32; x1++ {

			runeStr := runeIndexToString(runeIndex)
			runeIndex++

			fmt.Fprintln(buf, "\t\tfopix.RuneInfo{\n\t\t\tCharacter: "+runeStr+",\n\t\t\tBitmap: []string{")

			for y := 0; y < dy; y++ {
				fmt.Fprint(buf, "\t\t\t\t\"")
				for x := 0; x < dx; x++ {
					c := color.RGBAModel.Convert(m.At(x0+x1*dx+x, y0+y1*dy+y)).(color.RGBA)
					if rgbaColorIsBlack(c) {
						fmt.Fprint(buf, "-")
					} else {
						fmt.Fprint(buf, "0")
					}
				}
				fmt.Fprintln(buf, "\",")
			}
			fmt.Fprintln(buf, "\t\t\t},\n\t\t},")
		}
	}

	err = ioutil.WriteFile("result.txt", buf.Bytes(), os.ModePerm)
	if err != nil {
		log.Fatal(err)
	}
}

func rgbaColorIsBlack(c color.RGBA) bool {
	if c.R != 0 {
		return false
	}
	if c.G != 0 {
		return false
	}
	if c.B != 0 {
		return false
	}
	if c.A != 255 {
		return false
	}
	return true
}

func runeIndexToString(i int) string {

	if (0x20 <= i) && (i <= 0x7E) {
		return fmt.Sprintf("'%c'", i)
	}

	if r, ok := mapRunesGraphics[i]; ok {
		return fmt.Sprintf("%+q", r)
	}

	if r, ok := mapRunes[i]; ok {
		return fmt.Sprintf("%+q", r)
	}

	return ""
}

